/**
 * SerlSampleEfficiencyToy — ch4.3 "RL Post-Training: HIL-SERL in Sim" concept-toy
 * (demo id `serl_sample_efficiency`). THE sample-efficiency story, made honest as a
 * LEARNING CURVE — deliberately NOT a live side-by-side reach.
 *
 * WHY A CURVE, NOT A LIVE REACH. SERL's lesson is SAMPLE EFFICIENCY, not a stronger
 * final policy. On this small dense-reward task both arms top out near the same
 * short-horizon SAC ceiling (final-height gap is modest), so a live side-by-side
 * would show both arms reaching similarly and MISREPRESENT the point. The honest
 * visual is the curve: eval reach-distance vs ONLINE env samples, with the threshold
 * marked and the horizontal "0 vs ~10,000 online samples" gap as the hero. The
 * corrections-as-prior put HIL-SERL below the bar at ZERO online samples; from-scratch
 * SAC has to crawl there over ~10,000 online samples. That horizontal gap IS the win.
 *
 * ONE CONTROL — an online-sample BUDGET scrubber. Drag it and read, for each arm,
 * whether it has cleared the 0.10 m reach threshold WITHIN that many online samples.
 * HIL cleared at 0 (the prior); from-scratch only clears once the budget reaches
 * ~10,000. Scrub to ~5,000 and the contrast is stark: HIL long done, scratch still
 * searching. Default-interesting: boots at 5,000 (HIL cleared, scratch not yet).
 *
 * Real measured numbers from serl.py (seed 0 curve; seeds 0-2 ledger; cpu) —
 * regenerated by site/scripts/vizdata/ch4.3_serl.py into the co-located vizdata.json
 * (the per-step curve PARSED from serl.py's run output, every scalar cross-checked
 * against meta.yaml's reference_run, STOP-on-drift). So it is pure SVG + design
 * tokens: theme-aware for free, and the server-rendered default (both full curves +
 * threshold + both samples-to-threshold markers + the gap + the readout at the
 * default budget) IS the JS-off experience; hydration only makes the budget scrub.
 * Colour by MEANING: HIL-SERL = --signal blue (the sample-efficient hero, primed by
 * the corrections), from-scratch SAC = --alert red (the slow-to-learn baseline), the
 * reach threshold = --entity-target-ink green (the bar both aim for).
 *
 * Follows the FROZEN CONCEPT-TOY CONTRACT documented at the top of ../PlateIsland.tsx.
 */
